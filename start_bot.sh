# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è —Ñ–∞–π–ª–∞ .env
if [ ! -f .env ]; then
    echo "‚ùå –û—à–∏–±–∫–∞: —Ñ–∞–π–ª .env –Ω–µ –Ω–∞–π–¥–µ–Ω!"
    exit 1
fi

# –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞ –≤ —Ñ–æ–Ω–æ–≤–æ–º —Ä–µ–∂–∏–º–µ —Å –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏–µ–º
echo "üöÄ –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞..."
source .env
nohup python3 bot.py > logs/bot.log 2>&1 &
