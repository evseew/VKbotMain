    echo "‚úÖ –ü—Ä–æ—Ü–µ—Å—Å –±–æ—Ç–∞ —É—Å–ø–µ—à–Ω–æ —Ä–∞–±–æ—Ç–∞–µ—Ç"
    echo "üìä –í—ã–≤–æ–¥ –ª–æ–≥–æ–≤ (Ctrl+C –¥–ª—è –≤—ã—Ö–æ–¥–∞):"
    tail -f logs/bot.log
else
    echo "‚ùå –û—à–∏–±–∫–∞: –±–æ—Ç –Ω–µ —É–¥–∞–ª–æ—Å—å –∑–∞–ø—É—Å—Ç–∏—Ç—å, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –ª–æ–≥–∏: logs/bot.log"
    exit 1
fi